# PART 1: DATA TYPES

# Integer ('int') - Represents whole numbers without any decimal points.

num_one = 10
num_two = -5


# Float ('float') - Represents real numbers with decimal points.

pi = 3.14
radius = 2.5


# String ('str') - Represents a sequence of characters enclosed in single or double quotes.

name = "Alf the Cloud Guru"
dream = 'My dream is to be the greatest cloud architect in the world and provide everyone a solution using the cloud!'


# Boolean ('bool') - Represents a binary value: 'True' or 'False'.

is_valid = True
is_open = False
is_true = 0 # 0 is considered False.
is_false = 1 # Non-zero value is considered True.


# List ('list') - Represents an ordered, mutable sequence of elements.

even_numbers = [2, 4, 6, 8, 10]
odd_numbers = [1, 3, 5, 7, 9]
names = ["Alf", "AWS", "Cloud Computing"]


# Tuple ('tuple') - Similar to a list but immutable (cannot be changed after creation).

coordinates = (3, 4)
rgb_colors = ('red', 'green', 'blue')


# Dictionary ('dict') - Represents a collection of key-value pairs.

person_one = {'name': 'Alf', 'age': 0.5, 'gender': 'male'}
person_two = {'name': 'Alfina', 'age': 0.25, 'gender': 'female'}


# USES OF DICTIONARY DATA STRUCTURE

# Storing Key-Value Relationships
age = {'Alf': 0.5, 'Alfina': 0.25}

# Configuration Settings
config = {'debug': True, 'max_connections': 100, 'timeout': 30}

# Database-Like Operations
gurus = {
    'guru_one': {'name': 'Alf', 'age': 0.5, 'gender': 'male'},
    'guru_two': {'name': 'Alfina', 'age': 0.25, 'gender': 'female'}
}

# Representing JSON-Like Structures
cloud_guru = {'name': 'Alf', 'age': 0.5, 'gender': 'male'}








